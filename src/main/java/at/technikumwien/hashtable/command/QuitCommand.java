package at.technikumwien.hashtable.command;

import java.util.function.Consumer;

public class QuitCommand implements Runnable {

    final Consumer<Boolean> setQuit;

    // //////////////////////////////////////////////////////////////////////////
    // Init
    // //////////////////////////////////////////////////////////////////////////

    public QuitCommand(Consumer<Boolean> setQuit) {
        this.setQuit = setQuit;
    }

    // //////////////////////////////////////////////////////////////////////////
    // Methoden
    // //////////////////////////////////////////////////////////////////////////

    @Override
    public void run() {
        setQuit.accept(true);
    }
}
