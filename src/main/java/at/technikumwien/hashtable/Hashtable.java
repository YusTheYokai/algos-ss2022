package at.technikumwien.hashtable;

public class Hashtable {
    private Stock[] stocks;

    // //////////////////////////////////////////////////////////////////////////
    // Init
    // //////////////////////////////////////////////////////////////////////////

    public Hashtable(int size) {
        stocks = new Stock[size];
    }

    // //////////////////////////////////////////////////////////////////////////
    // Methoden
    // //////////////////////////////////////////////////////////////////////////

    public void add(Stock stock) {
        stocks[stock.hashCode() % stocks.length] = stock;
    }

    public Stock get(String name) {
        return stocks[Stock.hash(name) % stocks.length];
    }

}