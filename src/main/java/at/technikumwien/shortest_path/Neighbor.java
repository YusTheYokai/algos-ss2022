package at.technikumwien.shortest_path;

public class Neighbor {

    private final int cost;
    private final Node node;

    // //////////////////////////////////////////////////////////////////////////
    // Init
    // //////////////////////////////////////////////////////////////////////////

    public Neighbor(int cost, Node node) {
        this.cost = cost;
        this.node = node;
    }

    // //////////////////////////////////////////////////////////////////////////
    // Methoden
    // //////////////////////////////////////////////////////////////////////////

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }

        return hashCode() == obj.hashCode();
    }

    @Override
    public int hashCode() {
        return node.hashCode();
    }

    // //////////////////////////////////////////////////////////////////////////
    // Getter
    // //////////////////////////////////////////////////////////////////////////

    public int getCost() {
        return cost;
    }

    public Node getNode() {
        return node;
    }
}
