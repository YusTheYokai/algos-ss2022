package at.technikumwien.shortest_path;

public class Neighbor {

    private final int cost;
    private final Station station;

    // //////////////////////////////////////////////////////////////////////////
    // Init
    // //////////////////////////////////////////////////////////////////////////

    public Neighbor(int cost, Station station) {
        this.cost = cost;
        this.station = station;
    }

    // //////////////////////////////////////////////////////////////////////////
    // Getter
    // //////////////////////////////////////////////////////////////////////////

    public int getCost() {
        return cost;
    }

    public Station getStation() {
        return station;
    }
}
