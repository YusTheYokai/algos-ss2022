package at.technikumwien.shortest_path;

public class Neighbor {

    private final int cost;
    private final Node to;

    // //////////////////////////////////////////////////////////////////////////
    // Init
    // //////////////////////////////////////////////////////////////////////////

    public Neighbor(int cost, Node to) {
        this.cost = cost;
        this.to = to;
    }

    // //////////////////////////////////////////////////////////////////////////
    // Methoden
    // //////////////////////////////////////////////////////////////////////////

    @Override
    public boolean equals(Object obj) {
        if (obj == null) {
            return false;
        }

        return hashCode() == obj.hashCode();
    }

    @Override
    public int hashCode() {
        return to.hashCode();
    }

    // //////////////////////////////////////////////////////////////////////////
    // Getter
    // //////////////////////////////////////////////////////////////////////////

    public int getCost() {
        return cost;
    }

    public Node getTo() {
        return to;
    }
}
